// Also need to handle questions > q78 that we haven't seen individual options for.
// Let's check for remaining issues dynamically.

let fixCount = 0;
let remainingIssues = [];

for (const q of questions) {
  const correct = q.options.find(o => o.isCorrect);
  if (!correct) continue;
  
  for (const opt of q.options) {
    if (opt.isCorrect) continue;
    
    const key = `${q.id}:${opt.id}`;
    
    // Check if this option has the same explanation as the correct one
    const wrongEN = opt.explanation.toLowerCase().trim();
    const correctEN = correct.explanation.toLowerCase().trim();
    const wrongDE = opt.explanationDe.toLowerCase().trim();
    const correctDE = correct.explanationDe.toLowerCase().trim();
    
    const sameEN = wrongEN === correctEN;
    const sameDE = wrongDE === correctDE;
    
    if (sameEN || sameDE) {
      if (fixes[key]) {
        if (fixes[key].en) opt.explanation = fixes[key].en;
        if (fixes[key].de) opt.explanationDe = fixes[key].de;
        fixCount++;
      } else {
        remainingIssues.push({ key, optText: opt.text.slice(0, 50), prompt: q.prompt.slice(0, 60) });
      }
    }
    
    // Also check for remaining duplicate sentences
    const sentences = opt.explanation.split(/(?<=\.)\s+/);
    if (sentences.length >= 2) {
      for (let i = 0; i < sentences.length - 1; i++) {
        const a = sentences[i].toLowerCase().replace(/[^a-z ]/g, '').trim();
        const b = sentences[i+1].toLowerCase().replace(/[^a-z ]/g, '').trim();
        if (a.length > 15 && b.length > 15) {
          const wA = new Set(a.split(' '));
          const wB = new Set(b.split(' '));
          const common = [...wA].filter(w => wB.has(w));
          if (common.length / Math.max(wA.size, wB.size) > 0.55) {
            // Keep first sentence only
            opt.explanation = sentences[0];
            fixCount++;
          }
        }
      }
    }
  }
}

console.log(`Applied ${fixCount} fixes`);
if (remainingIssues.length > 0) {
  console.log(`\nRemaining unhandled: ${remainingIssues.length}`);
  remainingIssues.forEach(i => console.log(JSON.stringify(i)));
}

